package OBJETO;

public class ex1Herança {

	private String subirArvores;
	private String nome=null;
	 private int idade=0;
	 private String emiteSom=null;
	 private String corre=null;

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public int getIdade() {
		return idade;
	}

	public void setIdade(int idade) {
		this.idade = idade;
	}

	public String isEmiteSom() {
		return emiteSom;
	}

	public void setEmiteSom(String emiteSom) {
		this.emiteSom = emiteSom;
	}

	public String isCorre() {
		return corre;
	}

	public void setCorre(String corre) {
		this.corre = corre;
	}

	public String getSubirArvores() {
		return subirArvores;
	}

	public void setSubirArvores(String subirArvores) {
		this.subirArvores = subirArvores;
	}

	
	

}
